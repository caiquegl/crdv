<b>3. Disponibilidade de produtos </b>
<br>
<br>
Buscamos sempre manter as informações de disponibilidade de produtos em nosso site atualizadas de acordo com nossa disponibilidade real em estoque. No entanto, pelo fato de compartilharmos nosso estoque com nossa loja física e com nossos representantes comerciais (que podem realizar vendas e/ou reservas de produtos fora do site), existe a possibilidade de um produto se esgotar mas continuar aparecendo como "disponível" em nosso site até que seu status seja atualizado.
<br>
Além disso, no momento em que cada produto é embalado e preparado para envio, realizamos também uma análise para conferir se o mesmo encontra-se de acordo com o padrão de qualidade estabelecido pelo fabricante, afim de evitar transtornos para o cliente. Caso o produto não seja aprovado nesse processo de controle de estoque e seja a última peça disponível, o envio não poderá ser realizado.
<br>
Os procedimentos adotados quando um produto comprado em nosso site encontra-se indisponível no estoque são os seguintes:
<br>
Quando o pedido realizado pelo cliente for composto de APENAS UM ITEM e este item estiver indisponível em estoque, ofereceremos ao cliente um VALE COMPRAS no mesmo valor do produto indisponível. Se o cliente preferir, o valor total da compra (produto + frete) será estornado/devolvido INTEGRALMENTE, através de depósito bancário ou outra forma de pagamento à combinar.
<br>
Quando o pedido realizado pelo cliente for composto de MAIS DE UM ITEM e algum dos itens estiver indisponível em estoque, enviaremos normalmente todos os produtos disponíveis e ofereceremos ao cliente um VALE COMPRAS no mesmo valor do(s) produto(s) indisponível(is).
<br>
Se o cliente preferir, o valor correspondente ao valor do(s) produto(s) indisponível(is) será estornado/devolvido INTEGRALMENTE, através de depósito bancário ou outra forma de pagamento á combinar.
<br>
IMPORTANTE: A CRDV MAGAZINE investe constantemente em novas tecnologias para gerenciar seu estoque. Mas, como qualquer grande loja, não estamos livres de eventuais falhas operacionais, mesmo com todo cuidado e dedicação. Nosso principal objetivo é a honestidade e transparência no relacionamento com nossos clientes em situações adversas.
