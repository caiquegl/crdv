<b>1. Introdução</b>
<br>
<br>
Nossa POLÍTICA DE VENDAS foi elaborada integralmente de acordo com o Código de Defesa do Consumidor (Lei 8.078 de 11 de setembro de 1990), visando seguir as regras legais de relacionamento entre empresa e consumidores. Nos comprometemos a tratar todas as ocorrências de acordo com o que manda o Código de Defesa do Consumidor e a legislação brasileira. É importante que sejamos comunicados através de nossos canais de atendimento caso haja qualquer problema ou ocorrência no processo de pré-venda ou pós-venda, para que possamos analisar a situação e solucioná-la da melhor forma possível.
