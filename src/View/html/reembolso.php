<b>7. Garantia dos produtos </b>
<br>
<br>
Prazos e condições
<br>
Os prazos de garantia dos produtos comercializados pela CRDV MAGAZINE variam de acordo com o segmento de produto e fabricante, estando estas informações disponíveis no anúncio em nosso site e/ou manuais, termos de garantia, embalagens ou documentação do fabricante que acompanham o produto.
<br>
Trabalhamos com diversas marcas, fabricantes e distribuidores, nacionais e internacionais. Por isso, alguns produtos possuem garantia da loja e outros possuem garantia direta do fabricante.
<br>
Nos casos em que a garantia for oferecida diretamente pelo fabricante, seu prazo e forma de utilização são de total responsabilidade do próprio fabricante, de acordo com o termo de garantia que acompanha o produto.
<br>
Acionamento da garantia
<br>
Quando houver necessidade de acionamento da garantia o primeiro passo é entrar em contato através de um de nossos canais de atendimento para que seja feita a análise do caso, oferecendo então as orientações quanto ao tipo de garantia disponível para o produto em questão e os procedimentos necessários.
<br>
Veja todos os detalhes em nossa "política de trocas e devoluções".
