<b>2. Procedimento de entrega </b>
<br>
<br>
Prazo de POSTAGEM
<br>
Após a confirmação do pagamento seu pedido normalmente é postado em até 1 dia útil (24 horas), desde que a confirmação do pagamento ocorra dentro de nosso horário operacional.
<br>
Este prazo pode ser prorrogado em até 3 dias úteis, nas situações de instabilidades em sistemas fiscais e logísticos, grande fluxo de pedidos ocasionados por promoções, épocas comemorativas do ano e/ou lançamentos de produtos, ou devido a atrasos ocasionados por empresas terceirizadas (transportadoras, plataformas on-line, fornecedores etc.). Mesmo nessas situações, sempre nos esforçamos para postar seu produto com a máxima agilidade possível.
<br>
Horário operacional
<br>
Nosso horário operacional é das 09h às 18h, de segunda à sexta-feira (exceto em feriados municipais, estaduais ou nacionais).
<br>
Pedidos que tiverem confirmação de pagamento fora do horário operacional terão seu prazo de postagem contado à partir do próximo dia útil. Ou seja, se você realizar uma compra na sexta-feira e o seu pagamento for confirmado apenas após às 18h, o prazo de postagem passa a contar a partir da próxima segunda-feira.
<br>
Diferenças entre os prazos de POSTAGEM e de ENTREGA.|
<br>
Prazo de POSTAGEM: Período entre a realização da compra até o envio da encomenda para os Correios.
Prazo de ENTREGA: Período entre o recebimento da encomenda pelos Correios até o envio ao cliente.
<br>
Os prazos de ENTREGA variam de acordo com o endereço de destino e o método de entrega escolhido (Ex.: PAC, SEDEX, etc.).
<br>
Os prazos de ENTREGA anunciados no site são previsões aproximadas de acordo com informações repassadas pela empresa transportadora, podendo variar para mais ou para menos. Não nos responsabilizamos por eventuais atrasos ocasionados por falhas logísticas ou eventualidades no translado, ocorridos após a postagem da encomenda.
<br>
Horários de entrega
<br>
As entregas são realizadas pelas transportadoras em horário comercial, das 08h às 18h, de segunda à sexta-feira. As transportadoras não permitem o agendamento de data e/ou horário para entrega, devido às variadas rotas que são formuladas diariamente com diferentes pontos para entrega.
<br>
O cliente é responsável por informar no momento da finalização de cada compra os dados corretos do endereço para entrega dos produtos, bem como possibilitar seu recebimento, mantendo no local de entrega uma pessoa maior de 18 anos portando documento original de identidade para receber as mercadorias.
<br>
Não nos responsabilizamos por impossibilidade de entrega nos casos de endereço informado incorretamente ou ausência de pessoa qualificada para recebimento das mercadorias.
<br>
Tentativas de entrega
<br>
São realizadas três tentativas de entrega, sendo o produto encaminhado para a filial da transportadora após a terceira tentativa sem sucesso (ou seja, destinatário ausente), onde ficará armazenado aguardando retirada do cliente (se permitido pela empresa transportadora) ou de onde retornará para nossa loja. Caso exista erro no endereço informado pelo cliente a encomenda retornará imediatamente para nossa loja.
<br>
Caso o produto retorne para a nossa loja ele será reenviado ao cliente após novo pagamento pelo frete e eventuais acréscimos cobrados pela transportadora.
<br>
Verificação do(s) produto(s) e recusa da entrega
<br>
Orientamos o cliente a verificar os produtos no ato da entrega, na presença do entregador.
<br>
Caso seja identificado qualquer tipo de violação da embalagem ou invólucro do produto, avaria do produto ou mercadoria em desacordo com o pedido, o cliente deverá recusar o recebimento, indicando na Nota Fiscal e no documento de entrega o motivo da recusa, como por exemplo:
    <br>
"ENTREGA RECUSADA POR VIOLAÇÃO DA EMBALAGEM OU INVÓLUCRO DO PRODUTO";
<br>
"ENTREGA RECUSADA POR AVARIA DO PRODUTO";
<br>
"ENTREGA RECUSADA POR MERCADORIA EM DESACORDO COM O PEDIDO".

<br>
<br>

Após recusar o recebimento a encomenda retornará para nossa loja e o cliente deverá comunicar o fato através de um de nossos canais de atendimento imediatamente, para que seja analisada a ocorrência e tomadas as providências necessárias.
<br>
Ao assinar o documento de recebimento da transportadora sem nenhuma ressalva escrita o cliente confirma que a embalagem e o invólucro do produto não possuem avarias ou violação visíveis, assim como confirma o recebimento do produto de acordo com o pedido.
