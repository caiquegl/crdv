<b>4. Pagamentos </b>
<br>
<br>
Para as compras realizadas em nosso site aceitamos as seguintes formas de pagamento:
    <br>
Boleto (com desconto à vista): Pagamento aceito em toda a rede bancária até o vencimento;
<br>
Cartões de crédito (via MercadoPago): Parcelamento em até 3x sem juros e até 12x com juros, nos seguintes cartões: Visa, MasterCard, Hipercard, American Express, Diners Club International e Elo. Todas as formas de pagamento que aceitamos possuem confirmação automática, não havendo necessidade do cliente nos enviar qualquer tipo de comprovante, exceto se solicitado por nossa equipe. O envio de comprovantes sem solicitação prévia não agiliza o tempo de postagem ou a entrega do produto.
<br>
O tempo de confirmação do pagamento pode variar de acordo com o meio escolhido, sendo que através de cartões de crédito a confirmação leva em média de 1 à 24 horas (dependendo do valor) e em pagamentos via boleto a confirmação pode levar de 1 à 2 dias úteis.
<br>
O prazo de postagem da encomenda começa a valer apenas após a confirmação do pagamento.
<br>
Saiba mais em nossa página específica sobre "Formas de pagamento".
